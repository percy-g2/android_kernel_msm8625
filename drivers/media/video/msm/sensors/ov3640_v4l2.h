#define CMD_MAIN 0x3F00// main command Write only
#define CMD_TAG 0x3F01// command tag Write only
#define CMD_PARA3 0x3F02// parameter hh byte read/write
#define CMD_PARA2 0x3F03// parameter hl byte read/write
#define CMD_PARA1 0x3F04// parameter lh byte read/write
#define CMD_PARA0 0x3F05// parameter ll byte read/write
#define STA_ZONE 0x3F06// zone status read only,STA_ZONE is reserved
#define STA_FOCUS 0x3F07// focus status read only
/*
CMD_MAIN://If the command needs tag and parameters, the main command must be the last register to be sent.
0x03 Single focus mode,no CMD_TAG
0x05 Step mode, need CMD_TAG
0x08 Return to Idle mode, no CMD_TAG
0x10 Set Zone mode, need CMD_TAG
0x12 Update Zone, no CMD_TAG
0x20 Set Motor mode, need CMD_TAG

STA_FOCUS:
0xFF S_FIRWARE
Firmware is downloaded and not run.
0xFA S_STARTUP
Firmware is initializing.
0xFE S_ERROR
Unknown Firmware state
0xEE S_DRVICERR
Driver IC is not found.
0x00 S_IDLE
Idle state, lens is located at the furthest position.
0x01 S_FOCUSING
Auto Focus is running.
0x02 S_FOCUSED
Auto Focus is stopped.
0x12 S_CAPTURE
Read to capture
0x20 S_STEP
Manual mode
0x10 focused is completed

STA_ZONE: but it is ok just when first open camea
0: IDLE
!0:completed
*/


